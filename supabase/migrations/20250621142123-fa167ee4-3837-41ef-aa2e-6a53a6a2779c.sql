
-- Create SITI Cable TV packs table
CREATE TABLE public.siti_packs (
  id SERIAL PRIMARY KEY,
  pack_name TEXT NOT NULL,
  channel_count INTEGER NOT NULL,
  operator_price DECIMAL(10,2) NOT NULL, -- Price operator pays
  customer_price DECIMAL(10,2) NOT NULL, -- Price customer pays
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

-- Enable Row Level Security (public access for demo purposes)
ALTER TABLE public.siti_packs ENABLE ROW LEVEL SECURITY;

-- Create policy for public access (adjust based on your needs)
CREATE POLICY "Public access to SITI packs" 
  ON public.siti_packs 
  FOR ALL 
  USING (true)
  WITH CHECK (true);

-- Create trigger to automatically update updated_at
CREATE TRIGGER update_siti_packs_updated_at
    BEFORE UPDATE ON public.siti_packs
    FOR EACH ROW
    EXECUTE FUNCTION public.update_updated_at_column();

